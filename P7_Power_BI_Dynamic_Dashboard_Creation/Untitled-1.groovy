Rôles :
- DG : Supervisation de l'ensemble des projets, en plus des indicateurs clés ;
- DR : Supervisation des projets de sa région, et des pays la constituant, en plus des indicateurs clés ;
- DP : Supervisation des projets de son pays, en plus des indicateurs clés ;

Indicateurs clés :
- Couts des Projets ; Alert : +15%
- Durée des projets ; Alert : +15%
- Performance globale des projets via les livrables ; Alerte : -de 15%


User story 1 : En tant que DR, Je veux pouvoir suivre les indicateurs de délais et de livrables produits afin de suivre la performance globale de tous les projets.
User story 2 : En tant que DR, je souhaite être alerté lorsque les coûts de projets dépassent de 15% les coûts prévisionnels afin de décider de l’état des projets.
User story 3 : En tant que DR, je veux être alerté lorsque les livrables ne sont pas produits à hauteur de 15% de plus que prévu par rapport au planning prévisionnel afin de suivre la performance globale de tous les projets.


User story 4 : En tant que Directeur Régional, je veux être alerté lorsque les coûts et les deadlines dépassent de 15% les prévisions pour tous les projets de ma région afin d'intervenir auprès des directeurs pays.
User story 6 : En tant que Directeur Régional, je veux être alerté lorsque les livrables ne sont pas produits à hauteur de 15% de plus que prévu par rapport au planning prévisionnel pour tous les projets de ma région afin d'intervenir auprès des directeurs pays.


User story 1 : En tant que Directeur Général, je veux pouvoir superviser tous les projets du monde.
User story 4 : En tant que Directeur Régional, je veux pouvoir superviser tous les projets de ma région et les pays la constituant.
User story 7 : En tant que Directeur de Pays, je veux pouvoir superviser tous les projets de mon pays.

User story 8 : En tant que Directeur de Pays, je veux pouvoir suivre et être alerté des coûts, le respect des deadline et les livrables produits sur tous les projets de mon pays afin de pouvoir prendre des mesures correctives si nécessaire.
